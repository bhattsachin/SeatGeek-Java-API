package com.bhatt.seakgeek.model;

public class Stats {
	
	private float listing_count;
	private float average_price;
	private float lowest_price;
	private float hightest_price;
	
	
	public Stats() {
		super();
	}
	
	public float getListing_count() {
		return listing_count;
	}
	public void setListing_count(float listing_count) {
		this.listing_count = listing_count;
	}
	public float getAverage_price() {
		return average_price;
	}
	public void setAverage_price(float average_price) {
		this.average_price = average_price;
	}
	public float getLowest_price() {
		return lowest_price;
	}
	public void setLowest_price(float lowest_price) {
		this.lowest_price = lowest_price;
	}
	public float getHightest_price() {
		return hightest_price;
	}
	public void setHightest_price(float hightest_price) {
		this.hightest_price = hightest_price;
	}
	
	
	
	
}
